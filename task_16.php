<?php

	$n = rand(2, 10000);
	
	echo ("Совершенные числа в диапазоне от 2 до ".$n.": <br>");
	
	for($i = 2; $i < $n; $i++){
		
		$sum = 0;

		for($j = 1; $j <= ($i/2); $j++){
			
			if($i%$j == 0){
				
				$sum += $j;
			}
		}
		
		if($sum == $i){
			
			echo ($i."<br>");
		}
	}
	
?>
